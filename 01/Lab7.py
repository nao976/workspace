# 두개 정수를 입력 받고 합,차,곱,나눗셈의 결과를 출력하는 프로그램
# 첫번째 숫자 입력 받기
inputNumber1 = float(input("첫 번째 숫자를 입력하세요: "))
# 두번째 숫자 입력 받기
inputNumber2 = float(input("두 번째 숫자를 입력하세요: "))
# 각 계산 결과를 출력하기
print("합:",inputNumber1 + inputNumber2)
print("차:",inputNumber1 - inputNumber2)
print("곱:",inputNumber1 * inputNumber2)
print("나눗셈:",inputNumber1 / inputNumber2)